    private static long index = 1;

    public static void LoadDssvIntoTable() {
        try {
            list_sv = SinhVienDAO.getListSinhVien(); // lấy ra dssv
            if (list_sv != null) {  // nếu danh sách tồn tại thì load lên table
                list_sv.stream().map((s) -> {
                    dtm.addRow(new Object[]{
                        index, s.getMssv(), s.getTenSv(), false
                    });
                    return s;
                }).forEachOrdered((_item) -> {
                    index++;
                });
            }
        } catch (Exception e) {
            e.printStackTrace();
            return;
        }

    }

    public static void LoadDSMonHoc() {
        List<Monhoc> ds = MonHocDAO.GetListMonHoc();
        dcm = new DefaultComboBoxModel();
        dcm2 = new DefaultComboBoxModel();
        dcm3 = new DefaultComboBoxModel();
        if (ds != null) {
            ds.stream().map((d) -> {
                dcm.addElement(d.getMaMh());
                return d;
            }).map((d) -> {
                dcm2.addElement(d.getMaMh());
                return d;
            }).forEachOrdered((d) -> {
                dcm3.addElement(d.getMaMh());
            });

        }

    }

