    private void btnTaoMonHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTaoMonHocActionPerformed
        // kiểm tra có nội dung chưa được điền
        if (tbxMaMonHoc.getText().isEmpty() || tbxTenMonHoc.getText().isEmpty()
                || tbxYear.getText().isEmpty() || tbxPhongHoc.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin môn học và thời khóa biểu");
            return;
        } else { // đã điền đầy đủ thông tin
            if (tbxNgayKetThuc.getText().isEmpty()) { // chưa generate ngày kết thúc môn
                JOptionPane.showMessageDialog(this, "Vui lòng bấm xác nhận để lấy ngày kết thúc môn học");
                return;
            } else {
                Monhoc mh = MonHocDAO.getMonHocByMaMH(tbxMaMonHoc.getText()); // xem môn học đã tồn tại chưa
                if (mh == null) { // chưa tồn tại, sẽ tạo mới
                    mh = new Monhoc(tbxMaMonHoc.getText(), giaovu, tbxTenMonHoc.getText()); //tạo môn học mới từ mã,giáo vụ, tên mh
                    ThoikhoabieuId tkbid = new ThoikhoabieuId(tbxMaMonHoc.getText(), tbxTenMonHoc.getText());
                    String gioBatDau = String.valueOf(spnGBD.getValue()) + ":" + String.valueOf(spnPBD.getValue());
                    String gioKetThuc = String.valueOf(spnGKT.getValue()) + ":" + String.valueOf(spnPKT.getValue());
                    Thoikhoabieu tkb = new Thoikhoabieu(tkbid, mh, NBD, NKT, tbxThuTrongTuan.getText(), gioBatDau, gioKetThuc, tbxPhongHoc.getText());

                    String confirm = "Xác nhận tạo môn học: \r\n Mã môn học:" + mh.getMaMh()
                            + "\r\n " + mh.getTenMh()
                            + "\r\n Ngày bắt đầu:"
                            + NBD + "\r\n Ngày kết thúc: " + NKT + "\r\n " + tkb.getThuTrongTuan()
                            + "\r\n Giờ bắt đầu: "
                            + tkb.getGioBatDau() + "\r\n Giờ kết thúc: " + tkb.getGioKetThuc()
                            + "\r\n Phòng " + tkb.getTenPhongHoc();
                    int chon = JOptionPane.showConfirmDialog(this, confirm);
                    if (chon != JOptionPane.OK_OPTION) {
                        return;
                    }
                    boolean kq = MonHocDAO.SaveMonHoc(mh);
                    kq = ThoiKhoaBieuDAO.SaveThoiKhoaBieu(tkb);
                    if (kq) {
                        JOptionPane.showMessageDialog(this, "Tạo môn học thành công");
                        LoadDSMonHoc();

                        cbxChonMonHoc1.setModel(dcm);
                        cbxChonMonHoc1.repaint();

                        cbxCMH2.setModel(dcm2);
                        cbxCMH2.repaint();

                        cbxCMH3.setModel(dcm3);
                        cbxCMH3.repaint();
                        return;
                    } else {
                        JOptionPane.showMessageDialog(this, "Có lỗi trong quá trình tạo môn học");
                    }
                } else { // mã môn học đã tồn tại
                    JOptionPane.showMessageDialog(this, "Mã môn học này đã tồn tại. Vui lòng kiểm tra lại.");
                    return;
                }
            }
        }
    }//GEN-LAST:event_btnTaoMonHocActionPerformed

