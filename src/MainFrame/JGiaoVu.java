    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        int ngay = Integer.parseInt((String) cbxDay.getSelectedItem());
        int thang = Integer.parseInt((String) cbxMonth.getSelectedItem());
        long daytime = TimeUnit.DAYS.toMillis(15 * 7); // tính bao nhiêu ngày
        try {
            int nam = Integer.parseInt(tbxYear.getText());
            NBD = new Date(nam - 1900, thang - 1, ngay);
            NKT = new Date(NBD.getTime() + daytime);
            tbxNgayKetThuc.setText(NKT.toString());
            if ((NBD.getDay() + 1) == 1) {
                tbxThuTrongTuan.setText("Chủ Nhật");
            } else {
                tbxThuTrongTuan.setText("Thứ " + (NBD.getDay() + 1));

            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Năm bắt đầu chưa đúng định dạng. Xin kiểm tra lại");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

