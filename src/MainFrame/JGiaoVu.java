    private void btnXacNhanThayDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXacNhanThayDoiActionPerformed
        if (cbxCMH3.getSelectedIndex() != -1) {
            Dlgloading d = new Dlgloading(this, true);
            d.setModal(true);
            Thread t = new Thread() {
                public void run() {
                    String Mamh = (String) cbxCMH3.getSelectedItem(); // lấy mã môn học
                    String ncm = "";
                    int rows = tblDiemDanh.getRowCount(); // lấy số dòng của bảng
                    BangdiemdanhId bddid = null; // tạo id cho bảng điểm danh
                    Bangdiemdanh bdd = null; // tạo bảng điểm danh

                    for (int i = 0; i < rows; i++) { // duyệt qua số dòng
                        for (int j = 2; j < 17; j++) { // duyệt qua số cột điểm danh
                            if (dtm2.getValueAt(i, j) != null && (boolean) dtm2.getValueAt(i, j) == true) {
                                ncm = (ncm + String.valueOf(j - 1) + ",");
                            }
                        }
                        bddid = new BangdiemdanhId(Mamh, (String) dtm2.getValueAt(i, 0));
                        try {
                            bdd = BangDiemDanhDAO.getBDDByID(bddid);
                            bdd.setTuanCoMat(ncm);
                            if (BangDiemDanhDAO.UpdateBDD(bdd) == false) {
                                JOptionPane.showMessageDialog(null, "Cập nhật bảng điểm danh thất bại");
                                return;
                            }
                            ncm = "";
                        } catch (HeadlessException e) {
                            JOptionPane.showMessageDialog(null, "Cập nhật bảng điểm danh thất bại");
                        }

                    }
                    SwingUtilities.invokeLater(new Runnable() {//do swing work on EDT
                        public void run() {
                            d.dispose();
                        }
                    });
                }
            };
            t.start();
            d.setVisible(true);

        } else {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn Môn học");
        }

    }//GEN-LAST:event_btnXacNhanThayDoiActionPerformed

